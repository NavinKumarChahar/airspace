    icon: Icons.science,
    color: Colors.indigo,
    points: [
      'Dedicated lab space for experimentation.',
      'Collaboration with leading universities.',
      'Patenting new technologies.',
    ],
  ),
  _Section(
    title: 'Product Design',
    icon: Icons.design_services,
    color: Colors.pink,
