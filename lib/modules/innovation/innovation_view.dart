      'IoT ecosystem development.',
    ],
  ),
  _Section(
    title: 'Innovation Pipeline',
    icon: Icons.trending_up,
    color: Colors.lime,
    points: [
      'Quarterly hackathons and competitions.',
      'Idea incubation programs.',
      'Venture capital partnerships.',
    ],
  ),
  _Section(
    title: 'Market Disruption',
    icon: Icons.flash_on,
    color: Colors.amber,
